#!/usr/bin/env ruby

require './lib/factorint'

p factorint(10) # => [[2, 1], [5, 1]]: 2 * 5

puts factorint(10)
# =>
# 2
# 1
# 5
# 1

print factorint(10), "\n" # => [[2, 1], [5, 1]]

p factorint(20) # => [[2, 2], [5, 1]]: 2^2 * 5

puts factorint(20)
# =>
# 2
# 2
# 5
# 1

print factorint(20), "\n" # => [[2, 2], [5, 1]]

p factorint(20.1) # => []
puts factorint(20.1) # => (nil)
print factorint(20.1), "\n" # => []

