#!/usr/bin/env ruby

require './lib/fourop'

p fourop(10, 2) # => [12.0, 8.0, 20.0, 5.0]

puts fourop(10, 2)
# =>
# 12.0
# 8.0
# 20.0
# 5.0

print fourop(10, 2), "\n" # => [12.0, 8.0, 20.0, 5.0]

(1..10).each { |n| print fourop(n, n + 1), "\n" }
# =>
# [3.0, -1.0, 2.0, 0.5]
# [5.0, -1.0, 6.0, 0.6666666666666666]
# [7.0, -1.0, 12.0, 0.75]
# [9.0, -1.0, 20.0, 0.8]
